function getRandomActivity() {
    const randomIndex = Math.floor(Math.random() * activities.length);
    return activities[randomIndex];
}

// การทำงานเมื่อกดปุ่ม
document.getElementById("randomButton").addEventListener("click", () => {
    const activityBox = document.getElementById("activity-box");

    // ซ่อนข้อความเดิมด้วยอนิเมชัน
    activityBox.classList.add("hidden");

    // รอเวลาอนิเมชันเสร็จ (0.5 วินาที) แล้วเปลี่ยนข้อความ
    setTimeout(() => {
        const newActivity = getRandomActivity();
        activityBox.textContent = newActivity;

        // แสดงข้อความใหม่ด้วยอนิเมชัน
        activityBox.classList.remove("hidden");
    }, 400);
});
